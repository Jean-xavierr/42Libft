#include "libft.h"

size_t		ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	size_t	i;
	size_t	lengt;

	i = 0;
	lengt = ft_strlen(src);
	if (dstsize == 0)
		return (lengt);
	while (src[i] && i < dstsize - 1)
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (lengt);
}
