#include "libft.h"

int		ft_atoi(const char *str)
{
	int			i;
	int			nb;
	int			negative;

	i = 0;
	nb = 0;
	negative = 1;
	while ((str[i] && str[i] == ' ') || (str[i] >= 8 && str[i] <= 13))
		i++;
	if (str[i] == '-')
		negative = -1;
	if (str[i] == '-' || str[i] == '+')
		i++;
	while (str[i] && str[i] >= '0' && str[i] <= '9')
	{
		nb = nb * 10 + (str[i] - '0');
		i++;
	}
	return (nb * negative);
}
